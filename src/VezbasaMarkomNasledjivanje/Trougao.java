package VezbasaMarkomNasledjivanje;

public class Trougao {
    double a;
    double b;
    double c;


    public Trougao(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double obim() {
        return a + b + c;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;

    }
}


